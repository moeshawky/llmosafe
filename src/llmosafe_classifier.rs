const FNV_OFFSET: u64 = 0xcbf29ce484222325;
const FNV_PRIME: u64 = 0x00000100000001b3;
const MAX_TOKEN_LEN: usize = 256;

/// Yields FNV-1a 64-bit hashes for [a-zA-Z0-9]+ tokens and adjacent bigrams.
///
/// Tokenization is streaming: each call to `next()` produces one hash.
/// No allocation — the tokenizer borrows the input and yields hashes lazily.
/// Case-folds ASCII alphabetic characters. Max token length 256 bytes.
pub struct StreamingTokenizer<'a> {
    text: &'a [u8],
    pos: usize,
    prev_hash: u64,
    has_prev: bool,
    pending_unigram: Option<u64>,
}

impl<'a> StreamingTokenizer<'a> {
    pub fn new(text: &'a str) -> Self {
        Self {
            text: text.as_bytes(),
            pos: 0,
            prev_hash: 0,
            has_prev: false,
            pending_unigram: None,
        }
    }
}

impl<'a> Iterator for StreamingTokenizer<'a> {
    type Item = u64;

    /// Yields FNV-1a 64-bit token hashes one at a time.
    ///
    /// On each call:
    /// 1. If a pending unigram was deferred from a previous bigram emission,
    ///    returns it immediately and clears the buffer.
    /// 2. Scans input for the next alphanumeric token, skipping non-alphanumeric bytes.
    /// 3. Hashes the token byte-by-byte with case-folding (to ASCII lowercase),
    ///    capping at MAX_TOKEN_LEN=256 bytes.
    /// 4. If a previous token exists, computes and yields a bigram hash first
    ///    (prev_hash XOR 0x5F, times FNV_PRIME, XOR token_hash, times FNV_PRIME),
    ///    caching the unigram for the next call via `pending_unigram`.
    /// 5. If no previous token, yields the unigram directly.
    /// Returns None when input is exhausted.
    fn next(&mut self) -> Option<u64> {
        if let Some(h) = self.pending_unigram.take() {
            return Some(h);
        }

        loop {
            if self.pos >= self.text.len() {
                self.has_prev = false;
                return None;
            }

            let b = self.text[self.pos];
            if !b.is_ascii_alphanumeric() {
                self.pos += 1;
                continue;
            }

            let mut hash: u64 = FNV_OFFSET;
            let mut len: usize = 0;

            while self.pos < self.text.len() && self.text[self.pos].is_ascii_alphanumeric() {
                if len < MAX_TOKEN_LEN {
                    hash ^= self.text[self.pos].to_ascii_lowercase() as u64;
                    hash = hash.wrapping_mul(FNV_PRIME);
                    len += 1;
                }
                self.pos += 1;
            }

            let token_hash = hash;

            if self.has_prev {
                let mut bigram_hash = self.prev_hash;
                bigram_hash ^= 0x5F;
                bigram_hash = bigram_hash.wrapping_mul(FNV_PRIME);
                bigram_hash ^= token_hash;
                bigram_hash = bigram_hash.wrapping_mul(FNV_PRIME);

                self.prev_hash = token_hash;
                self.pending_unigram = Some(token_hash);
                return Some(bigram_hash);
            }

            self.prev_hash = token_hash;
            self.has_prev = true;
            return Some(token_hash);
        }
    }
}

