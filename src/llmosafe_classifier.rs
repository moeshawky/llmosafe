/// Output of `classify_text()`.
///
/// Fields:
/// - `score`: raw logistic regression sum (unbounded f32).
/// - `probability`: sigmoid of `score`, in (0, 1).
/// - `is_manipulation`: true when `score > THRESHOLD`.
/// - `oov_ratio`: proportion of tokens not found in VOCAB, [0.0, 1.0].
/// - `tokens_matched`: count of tokens found in VOCAB.
/// - `tokens_total`: total tokens processed by the tokenizer.
#[derive(Debug, Clone, Copy)]
pub struct ClassificationResult {
    /// Raw score = INTERCEPT + Σ(idf * coef). Unbounded.
    pub score: f32,
    /// sigmoid(score), in (0, 1).
    pub probability: f32,
    /// score > THRESHOLD (strict greater-than).
    pub is_manipulation: bool,
    /// 1.0 - (matched / total). For empty input, defaults to 1.0.
    pub oov_ratio: f32,
    /// Count of token hashes found in VOCAB.
    pub tokens_matched: u32,
    /// Total token hashes produced by the tokenizer.
    pub tokens_total: u32,
}

impl Default for ClassificationResult {
    /// Returns a ClassificationResult representing the empty-string fallback:
    /// score=INTERCEPT, probability=sigmoid(INTERCEPT),
    /// is_manipulation=(INTERCEPT > THRESHOLD, strict greater-than),
    /// oov_ratio=1.0, tokens_matched=0, tokens_total=0.
    fn default() -> Self {
        let probability = sigmoid(INTERCEPT);
        Self {
            score: INTERCEPT,
            probability,
            is_manipulation: INTERCEPT > THRESHOLD,
            oov_ratio: 1.0,
            tokens_matched: 0,
            tokens_total: 0,
        }
    }
}

/// Wraps `VOCAB.binary_search_by` with a closure that compares `hash`
/// against each entry's `hash` field. Returns `Ok(index)` if found,
/// `Err(insertion_point)` if not. VOCAB is a sorted slice of
/// `(u64, f32, f32)` tuples generated by build.rs.
#[inline]
fn binary_search_vocab(hash: u64) -> Result<usize, usize> {
    VOCAB.binary_search_by(|(h, _, _)| h.cmp(&hash))
}

/// Classify text as safe or manipulative using the embedded TF-IDF model.
///
/// Tokenizes input with `StreamingTokenizer`, matches against `VOCAB` via
/// binary search, computes `score = INTERCEPT + Σ(idf * coef)`, and returns
/// a `ClassificationResult` with probability, OOV ratio, and token counts.
///
/// Zero allocation. Runs in `no_std`.
pub fn classify_text(text: &str) -> ClassificationResult {
    let mut score: f32 = INTERCEPT;
    let mut matched: u32 = 0;
    let mut total: u32 = 0;

    for token_hash in StreamingTokenizer::new(text) {
        total += 1;
        if let Ok(idx) = binary_search_vocab(token_hash) {
            let (_hash, idf, coef) = VOCAB[idx];
            score += idf * coef;
            matched += 1;
        }
    }

    let probability = sigmoid(score);
    let oov_ratio = if total > 0 {
        1.0 - (matched as f32 / total as f32)
    } else {
        1.0
    };

    ClassificationResult {
        score,
        probability,
        is_manipulation: score > THRESHOLD,
        oov_ratio,
        tokens_matched: matched,
        tokens_total: total,
    }
}

